    static void ShowData() {
        //Eliminar todos los datos de la lista       
        Mlista.removeAllElements();

        //indice
        int i = 0;

        String d = (String) pi.Obtener(i);

        while (d != null) {

            Mlista.addElement(d);

            i++;
            d = (String) pi.Obtener(i);
        }

        if (Mlista.isEmpty() != true) {
            //Pila.setModel(Mlista);
            ListPila.setModel(Mlista);

        }
    }
