    private void btnPushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPushActionPerformed
        // TODO add your handling code here:
        if (txtCaptura.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "EL CAMPO DE TEXTO ESTA VACIO", "ERROR", JOptionPane.ERROR_MESSAGE, null);
            txtCaptura.grabFocus();
        } else {
            pi.push(txtCaptura.getText());
            ShowData();
            txtCaptura.setText("");
            txtCaptura.grabFocus();
        }
    }//GEN-LAST:event_btnPushActionPerformed

    private void btnPopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPopActionPerformed
        // TODO add your handling code here:
        if (pi.Tamano() >= 1) {
            pi.pop();

            ShowData();
            this.txtCaptura.grabFocus();
        } else {
            JOptionPane.showMessageDialog(this, "LA PILA ESTA VACIA", "ERROR", JOptionPane.ERROR_MESSAGE, null);
            txtCaptura.grabFocus();

        }
    }//GEN-LAST:event_btnPopActionPerformed

